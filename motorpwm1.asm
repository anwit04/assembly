ORG 0000H
ORG 00003
SETB P3.1;
CLR P3.0;
MAIN_LOOP:
SETB P3.5;
ACALL ON_DELAY;
CLR P3.5;
ACALL OFF_DELAY;
SJMP MAIN_LOOP;
ON_DELAY:
MOV R1,#7;
DELAY_1:
MOV R2,#255;
DELAY_2:
DJNZ R2,DELAY_1;
DJNZ R1,DELAY_2;
SJMP ON_DELAY;
RET;
OFF_DELAY:
MOV R1,#7;
DELAY_01:
MOV R2,#255;
DELAY_02:
DJNZ R2,DELAY_01;
DJNZ R1,DELAY_02;
SJMP OFF_DELAY;
RET;
END

