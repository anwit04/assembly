ORG 0000H
SETB P1.1;
CLR P1.2;
MAIN_LOOP:
SETB P1.0;
ACALL ON_DELAY;
CLR P1.0;
ACALL OFF_DELAY;
SJMP MAIN_LOOP;
ON_DELAY:
MOV R1,#7;
DELAY_1:
MOV R2,#255;
DELAY_2:
DJNZ R2,DELAY_1;
DJNZ R1,DELAY_2;
SJMP ON_DELAY;
RET;
OFF_DELAY:
MOV R1,#7;
DELAY_01:
MOV R2,#255;
DELAY_02:
DJNZ R2,DELAY_01;
DJNZ R1,DELAY_02;
SJMP OFF_DELAY;
RET;

